function generateGenreCheckboxes() {
    const genres = new Set();
    Object.values(window.games).forEach(game => {
        if (game.type) {
            game.type.forEach(genre => genres.add(genre));
        }
    });

    const genreFilters = document.querySelector('.genre-filters');
    genres.forEach(genre => {
        const label = document.createElement('label');
        const checkbox = document.createElement('input');
        checkbox.type = 'checkbox';
        checkbox.value = genre;
        checkbox.onchange = searchGames;

        label.appendChild(checkbox);
        label.appendChild(document.createTextNode(genre));
        genreFilters.appendChild(label);
    });
}

function getSelectedGenres() {
    const selectedGenres = [];
    const checkboxes = document.querySelectorAll('.genre-filters input[type="checkbox"]:checked');
    checkboxes.forEach(checkbox => {
        selectedGenres.push(checkbox.value);
    });
    return selectedGenres;
}

function searchGames() {
    const searchTerm = document.getElementById('game-search').value.toLowerCase();
    const selectedGenres = getSelectedGenres();
    populate(searchTerm, selectedGenres);
}

function openAboutMagic(url) {
    var urlObj = new window.URL(window.location.href);

    var win;
    if (win) {
        win.focus();
    } else {
        win = window.open();
        win.document.body.style.margin = '0';
        win.document.body.style.height = '100vh';
        var iframe = win.document.createElement('iframe');
        iframe.style.border = 'none';
        iframe.style.width = '100%';
        iframe.style.height = '100%';
        iframe.style.margin = '0';
        iframe.src = url;
        win.document.body.appendChild(iframe);


        var interval = setInterval(function () {
            if (win.closed) {
                clearInterval(interval);
                win = undefined;

            }
        }, 500);


    }
}


function populate(searchTerm = '', selectedGenres = []) {
    
    const gameList = document.querySelector('.game-list');
    gameList.innerHTML = '';

    Object.entries(window.games)
    .sort((a, b) => a[0].localeCompare(b[0]))
    .forEach(([gameName, gameDetails]) => {

        if (searchTerm && !gameDetails.description.toLowerCase().includes(searchTerm)) {
            return;
        }
        
        if (selectedGenres.length > 0 && gameDetails.type) {
            const gameGenres = gameDetails.type.map(genre => genre.toLowerCase());
            const hasMatchingGenre = selectedGenres.some(selectedGenre => gameGenres.includes(selectedGenre.toLowerCase()));
            if (!hasMatchingGenre) {
                return;
            }
        }

        const gamePanel = document.createElement('div');
        gamePanel.className = 'game-panel';

        const panelTop = document.createElement('div');
        panelTop.className = 'panel-top';

        const img = document.createElement('img');
        img.src = gameDetails.image;
        img.alt = gameName;

        const panelBottom = document.createElement('div');
        panelBottom.className = 'panel-bottom';

        const description = document.createElement('h2');
        description.className = 'game-description';
        description.textContent = gameDetails.description;

        const playButton = document.createElement('a');
        playButton.className = 'btn';
        playButton.href = "javascript:openAboutMagic('" + gameDetails.url + "')";
        playButton.textContent = 'Play Now';

        panelTop.appendChild(img);
        panelBottom.appendChild(description);
        panelBottom.appendChild(playButton);

        gamePanel.appendChild(panelTop);
        gamePanel.appendChild(panelBottom);

        gameList.appendChild(gamePanel);
    });
}

generateGenreCheckboxes();
